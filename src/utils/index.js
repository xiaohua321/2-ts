/**
 * 计算两个参数的和
 * @param {number} a - 第一个参数
 * @param {number} b - 第二个参数
 * @returns {number} 两个参数的和
 */
export function add (a, b) {
  return a + b
}

/**
 * 计算两个参数的差
 * @param {number} a - 被减数
 * @param {number} b - 减数
 * @returns {number} 两个参数的差
 */
export function subtract (a, b) {
  return a - b
}
